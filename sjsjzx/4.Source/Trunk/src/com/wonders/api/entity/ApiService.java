package com.wonders.api.entity;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Name;
import org.nutz.dao.entity.annotation.Table;

@Table("API_SERVICE")
public class ApiService {
	@Name
	@Column("ID")
	private String id;
	@Column("SERVICE_NAME")
	private String serviceName;
	@Column("URL")
	private String url;
	@Column("SUMMARY")
	private String summary;
	@Column("INSTRUCTION")
	private String instruction;
	@Column("VALID")
	private String valid;
	@Column("FUNCTION")
	private String function;
	@Column("PARAM")
	private String param;
	@Column("RETURN_PARAM")
	private String returnParam;
	@Column("API_TYPE")
	private String apiType;
	
	private ApiServiceUser serviceUser;
	
	private long callresult;
	
	private int calltime;
	
	
	public String getApiType() {
		return apiType;
	}
	public void setApiType(String apiType) {
		this.apiType = apiType;
	}
	public long getCallresult() {
		return callresult;
	}
	public void setCallresult(long callresult) {
		this.callresult = callresult;
	}
	public int getCalltime() {
		return calltime;
	}
	public void setCalltime(int calltime) {
		this.calltime = calltime;
	}
	public ApiServiceUser getServiceUser() {
		return serviceUser;
	}
	public void setServiceUser(ApiServiceUser serviceUser) {
		this.serviceUser = serviceUser;
	}
	public String getFunction() {
		return function;
	}
	public void setFunction(String function) {
		this.function = function;
	}
	public String getParam() {
		return param;
	}
	public void setParam(String param) {
		this.param = param;
	}
	public String getReturnParam() {
		return returnParam;
	}
	public void setReturnParam(String returnParam) {
		this.returnParam = returnParam;
	}
	public String getValid() {
		return valid;
	}
	public void setValid(String valid) {
		this.valid = valid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getInstruction() {
		return instruction;
	}
	public void setInstruction(String instruction) {
		this.instruction = instruction;
	}
	
}
