package com.wonders.pzgl;

import java.util.Comparator;

import com.wonders.pzgl.entity.ColRelationConfig;

public class ListNumComparator  implements Comparator<ColRelationConfig>{

	@Override
	public int compare(ColRelationConfig o1, ColRelationConfig o2) {
		int flag = o1.getListNum().compareTo(o2.getListNum());
		return flag;
	}

}
