package com.wonders.jh.entity.serve;

import java.util.Date;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Name;
import org.nutz.dao.entity.annotation.Table;

/**
 * 
 *流程定义对象
 */
@Table("FLOW_WORK_INFO")
public class FlowWorkInfo implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	@Name
	@Column("WORK_ID")
	private String workId;

	@Column("TASK_ID")
	private String taskId;
	
	@Column("FLOW_NO")
	private String flowNo;
	
	@Column("NODE_NO")
	private String nodeNo;
	
	@Column("BUSINESS_NO")
	private String businessNo;
	
	@Column("NODE_STATUS_CODE")
	private String nodeStatusCode;
	
	@Column("CREATE_BY")
	private String createBy;
	
	@Column("CREATE_DATE")
	private Date createDate;
	
	@Column("IS_VALID")
	private String isValid;
	
	@Column("WORK_FROM")
	private String workFrom;
	
	@Column("WORK_TO")
	private String workTo;
	
	public FlowWorkInfo(String workId, String taskId, String flowNo,
			String nodeNo, String businessNo, String nodeStatusCode,
			String createBy, Date createDate, String isValid, String workFrom,
			String workTo) {
		super();
		this.workId = workId;
		this.taskId = taskId;
		this.flowNo = flowNo;
		this.nodeNo = nodeNo;
		this.businessNo = businessNo;
		this.nodeStatusCode = nodeStatusCode;
		this.createBy = createBy;
		this.createDate = createDate;
		this.isValid = isValid;
		this.workFrom = workFrom;
		this.workTo = workTo;
	}

	public FlowWorkInfo() {
		super();
		// TODO Auto-generated constructor stub
	}

	public String getWorkId() {
		return workId;
	}

	public void setWorkId(String workId) {
		this.workId = workId;
	}

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public String getFlowNo() {
		return flowNo;
	}

	public void setFlowNo(String flowNo) {
		this.flowNo = flowNo;
	}

	public String getNodeNo() {
		return nodeNo;
	}

	public void setNodeNo(String nodeNo) {
		this.nodeNo = nodeNo;
	}

	public String getBusinessNo() {
		return businessNo;
	}

	public void setBusinessNo(String businessNo) {
		this.businessNo = businessNo;
	}

	public String getNodeStatusCode() {
		return nodeStatusCode;
	}

	public void setNodeStatusCode(String nodeStatusCode) {
		this.nodeStatusCode = nodeStatusCode;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getIsValid() {
		return isValid;
	}

	public void setIsValid(String isValid) {
		this.isValid = isValid;
	}

	public String getWorkFrom() {
		return workFrom;
	}

	public void setWorkFrom(String workFrom) {
		this.workFrom = workFrom;
	}

	public String getWorkTo() {
		return workTo;
	}

	public void setWorkTo(String workTo) {
		this.workTo = workTo;
	}
	
}
