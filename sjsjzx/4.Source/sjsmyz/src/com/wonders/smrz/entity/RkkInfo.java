package com.wonders.smrz.entity;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Table;

@Table("T_GA_RJBXX")
public class RkkInfo {

	/**
	 * 身份证号码
	 */
	@Column("ZJHM")
	private String	zjhm;
	/**
	 * 姓名
	 */
	@Column("XM")
	private String	xm;
	/**
	 * 户籍地址门牌
	 */
	@Column("HJDYJMP")
	private String	hjdyjmp;
	/**
	 * 配偶姓名
	 */
	@Column("POXM")
	private String	poxm;
	/**
	 * 民族
	 */
	@Column("MZHZ")
	private String	mzhz;
	/**
	 * 文化程度
	 */
	@Column("WHCDHZ")
	private String	whcdhz;
	/**
	 * 血型
	 */
	@Column("XXHZ")
	private String	xxhz;
	
	public String getZjhm() {
		return zjhm;
	}
	public void setZjhm(String zjhm) {
		this.zjhm = zjhm;
	}
	public String getXm() {
		return xm;
	}
	public void setXm(String xm) {
		this.xm = xm;
	}
	public String getHjdyjmp() {
		return hjdyjmp;
	}
	public void setHjdyjmp(String hjdyjmp) {
		this.hjdyjmp = hjdyjmp;
	}
	public String getPoxm() {
		return poxm;
	}
	public void setPoxm(String poxm) {
		this.poxm = poxm;
	}
	public String getMzhz() {
		return mzhz;
	}
	public void setMzhz(String mzhz) {
		this.mzhz = mzhz;
	}
	public String getWhcdhz() {
		return whcdhz;
	}
	public void setWhcdhz(String whcdhz) {
		this.whcdhz = whcdhz;
	}
	public String getXxhz() {
		return xxhz;
	}
	public void setXxhz(String xxhz) {
		this.xxhz = xxhz;
	}
	
	
	
}
