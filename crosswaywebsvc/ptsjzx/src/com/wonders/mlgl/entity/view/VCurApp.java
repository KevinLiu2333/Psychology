package com.wonders.mlgl.entity.view;

import java.util.Date;
import org.nutz.dao.entity.annotation.*;

@View("V_CUR_APP")
public class VCurApp {

    @Name
    @Column("ST_APPLY_ID")
	private String stApplyId; 
    /**
	 * 申请日期
	 */
    @Column("DT_APPLY")
	private Date dtApply; 
    /**
	 * 资源提供单位
	 */    
    @Column("ST_SOURCE_PROVIDER")
	private String stSourceProvider; 
    /**
	 * 申请批次
	 */
    @Column("ST_APP_SERIEL_NO")
	private String stAppSeriel; 
    /**
	 * 办理意见
	 * 1：同意；0：不同意；2：退回
	 */
    @Column("IF_AGREE")
	private String ifAgree; 
    /**
	 * 资源申请单位
	 */
    @Column("ST_APPLICANT")
	private String stApplicant; 
    /**
	 * 申请人
	 */    
    @Column("ST_APPLIER_ID")
	private String stApplierId;
    /**
	 * 备注
	 */    
    @Column("MEMO")
	private String memo;
    
    @Column("ST_STATUS")
	private String stStatus;
    
    
	public String getStStatus() {
		return stStatus;
	}
	public void setStStatus(String stStatus) {
		this.stStatus = stStatus;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getStApplyId() {
		return stApplyId;
	}
	public void setStApplyId(String stApplyId) {
		this.stApplyId = stApplyId;
	}
	public Date getDtApply() {
		return dtApply;
	}
	public void setDtApply(Date dtApply) {
		this.dtApply = dtApply;
	}
	public String getStSourceProvider() {
		return stSourceProvider;
	}
	public void setStSourceProvider(String stSourceProvider) {
		this.stSourceProvider = stSourceProvider;
	}
	public String getStAppSeriel() {
		return stAppSeriel;
	}
	public void setStAppSeriel(String stAppSeriel) {
		this.stAppSeriel = stAppSeriel;
	}
	public String getIfAgree() {
		return ifAgree;
	}
	public void setIfAgree(String ifAgree) {
		this.ifAgree = ifAgree;
	}
	public String getStApplicant() {
		return stApplicant;
	}
	public void setStApplicant(String stApplicant) {
		this.stApplicant = stApplicant;
	}
	public String getStApplierId() {
		return stApplierId;
	}
	public void setStApplierId(String stApplierId) {
		this.stApplierId = stApplierId;
	}  
	
	
	@Column("WF_CODE")
	private String wfCode;
	@Column("CUR_NODE_NAME")
	private String curNodeName;
	@Column("TUDO_USER_ID")
	private String tudoUserId;
	@Column("SIGN_USER_ID")
	private String signUserId;
	@Column("INSTANCE_ID")
	private String instanceId;
	@Column("CUR_NODE_ID")
	private String curNodeId;
	@Column("OP_ALL_USER_IDS")
	private String opAllUserIds;
	@Column("STATUS")
	private String status;
	@Column("VALIDITY")
	private String validity;
	@Column("FIN_PRE_NODE_ID")
	private String finPreNodeId;


	public String getWfCode() {
		return wfCode;
	}
	public void setWfCode(String wfCode) {
		this.wfCode = wfCode;
	}
	public String getCurNodeName() {
		return curNodeName;
	}
	public void setCurNodeName(String curNodeName) {
		this.curNodeName = curNodeName;
	}
	public String getTudoUserId() {
		return tudoUserId;
	}
	public void setTudoUserId(String tudoUserId) {
		this.tudoUserId = tudoUserId;
	}
	public String getSignUserId() {
		return signUserId;
	}
	public void setSignUserId(String signUserId) {
		this.signUserId = signUserId;
	}
	public String getInstanceId() {
		return instanceId;
	}
	public void setInstanceId(String instanceId) {
		this.instanceId = instanceId;
	}
	public String getCurNodeId() {
		return curNodeId;
	}
	public void setCurNodeId(String curNodeId) {
		this.curNodeId = curNodeId;
	}
	public String getOpAllUserIds() {
		return opAllUserIds;
	}
	public void setOpAllUserIds(String opAllUserIds) {
		this.opAllUserIds = opAllUserIds;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getValidity() {
		return validity;
	}
	public void setValidity(String validity) {
		this.validity = validity;
	}
	public String getFinPreNodeId() {
		return finPreNodeId;
	}
	public void setFinPreNodeId(String finPreNodeId) {
		this.finPreNodeId = finPreNodeId;
	}
	
    
}
