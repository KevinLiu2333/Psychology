package com.wonders.tiles.autocrud.entity;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Name;
import org.nutz.dao.entity.annotation.Table;

/**
* 
*/
@Table("QUERY_APP_COLUMN")
public class AppColumn {

	/** 字段编号 */
	@Name
	@Column("COL_ID")
	private String colId;
	
	/** 表编号 */
	@Column("TABLE_ID")
	private String tableId;
	
	/** 字段名称（中文） */
	@Column("NAME")
	private String name;
	
	/** 字段名称（字母） */
	@Column("NAME_LETTER")
	private String nameLetter;
	
	/** 字段初始值-主要用于PK */
	@Column("INIT_VALUE")
	private String initValue;
	
	/** 编辑类型 1:字符; 2:数据; 3:字典；4:时间; */
	@Column("DATA_TYPE")
	private Integer dataType;
	
	/** 数值型字段单位 */
	@Column("DATA_UNIT")
	private String dataUnit;
	
	/** 字段长度 */
	@Column("DATA_LENGTH")
	private Integer dataLength;
	
	/** 如果是字典型,对应于DIC_CONFIG表中的DIC_ID的值 */
	@Column("DIC_ID")
	private Integer dicId;
	
	/** 是否为必录项 */
	@Column("IS_MANDATORY")
	private String isMandatory;
	
	/** 是否为主健 */
	@Column("IS_PK")
	private String isPk;
	
	/** 是否为外键 */
	@Column("IS_FK")
	private String isFk;
	
	/** 是否作为列表字段 */
	@Column("IS_LIST_COL")
	private String isListCol;
	
	/** 是否作为查询条件 */
	@Column("IS_QUERY_COL")
	private String isQueryCol;
	
	/** 是否遗传 */
	@Column("IS_INHERIT")
	private String isInherit;
	
	/** 是否显示 */
	@Column("IS_SHOW")
	private String isShow;
	
	 /** 显示顺序 */
	@Column("LIST_NUM")
	private Integer listNum;
	
	/** 有效性 1-有效 0-无效 */
	@Column("VALIDITY")
	private String validity;
	
    /** 值-用于保存数据库查询的值 */
    private String value;
	
	public String getColId() {
		return colId;
	}
	public void setColId(String colId) {
		this.colId = colId;
	}
	
	public String getTableId() {
		return tableId;
	}
	public void setTableId(String tableId) {
		this.tableId = tableId;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getNameLetter() {
		return nameLetter;
	}
	public void setNameLetter(String nameLetter) {
		this.nameLetter = nameLetter;
	}
	
	public String getInitValue() {
		return initValue;
	}
	public void setInitValue(String initValue) {
		this.initValue = initValue;
	}
	
	public Integer getDataType() {
		return dataType;
	}
	public void setDataType(Integer dataType) {
		this.dataType = dataType;
	}
	
	public String getDataUnit() {
		return dataUnit;
	}
	public void setDataUnit(String dataUnit) {
		this.dataUnit = dataUnit;
	}
	
	public Integer getDataLength() {
		return dataLength;
	}
	public void setDataLength(Integer dataLength) {
		this.dataLength = dataLength;
	}
	
	public Integer getDicId() {
		return dicId;
	}
	public void setDicId(Integer dicId) {
		this.dicId = dicId;
	}
	
	public String getIsMandatory() {
		return isMandatory;
	}
	public void setIsMandatory(String isMandatory) {
		this.isMandatory = isMandatory;
	}
	
	public String getIsPk() {
		return isPk;
	}
	public void setIsPk(String isPk) {
		this.isPk = isPk;
	}
	
	public String getIsFk() {
		return isFk;
	}
	public void setIsFk(String isFk) {
		this.isFk = isFk;
	}
	
	public String getIsListCol() {
		return isListCol;
	}
	public void setIsListCol(String isListCol) {
		this.isListCol = isListCol;
	}
	
	public String getIsQueryCol() {
		return isQueryCol;
	}
	public void setIsQueryCol(String isQueryCol) {
		this.isQueryCol = isQueryCol;
	}
	
	public String getIsInherit() {
		return isInherit;
	}
	public void setIsInherit(String isInherit) {
		this.isInherit = isInherit;
	}
	
	public String getIsShow() {
		return isShow;
	}
	public void setIsShow(String isShow) {
		this.isShow = isShow;
	}
	
	public Integer getListNum() {
		return listNum;
	}
	public void setListNum(Integer listNum) {
		this.listNum = listNum;
	}
	
	public String getValidity() {
		return validity;
	}
	public void setValidity(String validity) {
		this.validity = validity;
	}
	
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
}