package com.wonders.tiles.workflow.exception;

/**
 * 工作流异常
 *
 */
public class WfException extends RuntimeException {
	
	public WfException() {
		super();
	}

	public WfException(Throwable ex) {
		super(ex);
	}

	public WfException(String msg) {
		super(msg);
	}

	public WfException(String msg, Throwable ex) {
		super(msg, ex);
	}


}
