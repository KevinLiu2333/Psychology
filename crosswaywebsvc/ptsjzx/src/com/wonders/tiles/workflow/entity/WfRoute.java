package com.wonders.tiles.workflow.entity;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Table;

/**
 * 流程路由对象
 */

@Table("WF_ROUTE")
public class WfRoute implements java.io.Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	@Column("ROUTE_ID")
	private String routeId;

	@Column("WF_CODE")
	private String wfCode;

	@Column("NODE_ID")
	private String nodeId;

	@Column("NEXT_NODE_ID")
	private String nextNodeId;

	@Column("CON_KEYS")
	private String conKeys;

	@Column("CON_VALUES")
	private String conValues;

	@Column("IS_CON")
	private String isCon;

	@Column("VALIDITY")
	private String validity;

	
	@Column("IS_SEND_OUT")
	private String isSendOut;

	@Column("IS_NEED")
	private String isNeed;
	
	@Column("PART_JOIN_TYPE")
	private String partJoinType;

	public WfRoute() {
	}

	public WfRoute(String routeId) {
		this.routeId = routeId;
	}

	public String getRouteId() {
		return this.routeId;
	}

	public void setRouteId(String routeId) {
		this.routeId = routeId;
	}

	public String getWfCode() {
		return this.wfCode;
	}

	public void setWfCode(String wfCode) {
		this.wfCode = wfCode;
	}

	public String getNodeId() {
		return this.nodeId;
	}

	public void setNodeId(String nodeId) {
		this.nodeId = nodeId;
	}

	public String getNextNodeId() {
		return this.nextNodeId;
	}

	public void setNextNodeId(String nextNodeId) {
		this.nextNodeId = nextNodeId;
	}

	public String getConKeys() {
		return this.conKeys;
	}

	public void setConKeys(String conKeys) {
		this.conKeys = conKeys;
	}

	public String getConValues() {
		return this.conValues;
	}

	public void setConValues(String conValues) {
		this.conValues = conValues;
	}

	public String getIsCon() {
		return this.isCon;
	}

	public void setIsCon(String isCon) {
		this.isCon = isCon;
	}

	public String getValidity() {
		return this.validity;
	}

	public void setValidity(String validity) {
		this.validity = validity;
	}


	public String getIsNeed() {
		return isNeed;
	}

	public void setIsNeed(String isNeed) {
		this.isNeed = isNeed;
	}

	
	public String getIsSendOut() {
		return isSendOut;
	}

	public void setIsSendOut(String isSendOut) {
		this.isSendOut = isSendOut;
	}

	public String getPartJoinType() {
		return partJoinType;
	}

	public void setPartJoinType(String partJoinType) {
		this.partJoinType = partJoinType;
	}
	

}
