package com.wonders.jh.entity.serve;

import java.util.Date;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Name;
import org.nutz.dao.entity.annotation.Table;

/**
 * 
 *流程定义对象
 */
@Table("FLOW_WORK_LOG")
public class FlowWorkLog implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	@Name
	@Column("LOG_ID")
	private String logId;
	
	@Column("WORK_ID")
	private String workId;

	@Column("TASK_ID")
	private String taskId;
	
	@Column("FLOW_NO")
	private String flowNo;
	
	@Column("NODE_NO")
	private String nodeNo;
	
	@Column("BUSINESS_NO")
	private String businessNo;
	
	@Column("NODE_STATUS_CODE")
	private String nodeStatusCode;
	
	@Column("HANDLE_NOTES")
	private String handleNotes;

	@Column("HANDLE_DATE")
	private Date handleDate;
	
	@Column("CREATE_BY")
	private String createBy;
	
	@Column("CREATE_DATE")
	private Date createDate;
	
	@Column("IS_VALID")
	private String isValid;

	public FlowWorkLog() {
		super();
		// TODO Auto-generated constructor stub
	}


	public FlowWorkLog(String logId,String workId, String taskId, String flowNo,
			String nodeNo, String businessNo, String nodeStatusCode,
			String handleNotes, Date handleDate, String createBy,
			Date createDate, String isValid) {
		super();
		this.logId = logId;
		this.workId = workId;
		this.taskId = taskId;
		this.flowNo = flowNo;
		this.nodeNo = nodeNo;
		this.businessNo = businessNo;
		this.nodeStatusCode = nodeStatusCode;
		this.handleNotes = handleNotes;
		this.handleDate = handleDate;
		this.createBy = createBy;
		this.createDate = createDate;
		this.isValid = isValid;
	}


	public String getWorkId() {
		return workId;
	}

	public void setWorkId(String workId) {
		this.workId = workId;
	}

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public String getFlowNo() {
		return flowNo;
	}

	public void setFlowNo(String flowNo) {
		this.flowNo = flowNo;
	}

	public String getNodeNo() {
		return nodeNo;
	}

	public void setNodeNo(String nodeNo) {
		this.nodeNo = nodeNo;
	}

	public String getBusinessNo() {
		return businessNo;
	}

	public void setBusinessNo(String businessNo) {
		this.businessNo = businessNo;
	}

	public String getNodeStatusCode() {
		return nodeStatusCode;
	}

	public void setNodeStatusCode(String nodeStatusCode) {
		this.nodeStatusCode = nodeStatusCode;
	}

	public String getHandleNotes() {
		return handleNotes;
	}

	public void setHandleNotes(String handleNotes) {
		this.handleNotes = handleNotes;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getIsValid() {
		return isValid;
	}

	public void setIsValid(String isValid) {
		this.isValid = isValid;
	}


	public Date getHandleDate() {
		return handleDate;
	}


	public void setHandleDate(Date handleDate) {
		this.handleDate = handleDate;
	}


	public String getLogId() {
		return logId;
	}


	public void setLogId(String logId) {
		this.logId = logId;
	}
	
	
}
