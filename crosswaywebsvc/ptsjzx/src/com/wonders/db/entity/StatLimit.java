package com.wonders.db.entity;

public class StatLimit {

    /**
     * 次数
     */
    private int sums;

    /**
     * 次数
     */
    private String cm;


    /**
     *数量
     */
    private int counts;


	public int getSums() {
		return sums;
	}


	public void setSums(int sums) {
		this.sums = sums;
	}


	public int getCounts() {
		return counts;
	}


	public void setCounts(int counts) {
		this.counts = counts;
	}


	public String getCm() {
		return cm;
	}


	public void setCm(String cm) {
		this.cm = cm;
	}


}
