package com.wonders.log.entity;

import java.util.Date;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Table;
/**
 * @author dwl
 *系统日志
 */
@Table("LOG_SYSTEM")
public class SystemLog {
	/**
	*日志id
	**/
	@Column("ID")
	private String id;
	/**
	*操作类型
	**/
	@Column("OPERATE_TYPE")
	private String operatetype;
	/**
	*操作时间
	**/
	@Column("OPERATE_DATE")
	private Date operatedate;
	/**
	*操作用户
	**/
	@Column("OPERATE_USER")
	private String operateuser;
	/**
	*用户中文名
	**/
	@Column("USER_ZWNAME")
	private String userzwname;
	/**
	*操作部门
	**/
	@Column("OPERATE_DEPT")
	private String operatedept;
	/**
	*操作内容
	**/
	@Column("OPERATE_CONTENT")
	private String operatecontent;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOperatetype() {
		return operatetype;
	}
	public void setOperatetype(String operatetype) {
		this.operatetype = operatetype;
	}
	public Date getOperatedate() {
		return operatedate;
	}
	public void setOperatedate(Date operatedate) {
		this.operatedate = operatedate;
	}
	public String getOperateuser() {
		return operateuser;
	}
	public void setOperateuser(String operateuser) {
		this.operateuser = operateuser;
	}
	public String getUserzwname() {
		return userzwname;
	}
	public void setUserzwname(String userzwname) {
		this.userzwname = userzwname;
	}
	public String getOperatedept() {
		return operatedept;
	}
	public void setOperatedept(String operatedept) {
		this.operatedept = operatedept;
	}
	public String getOperatecontent() {
		return operatecontent;
	}
	public void setOperatecontent(String operatecontent) {
		this.operatecontent = operatecontent;
	}
	
}
