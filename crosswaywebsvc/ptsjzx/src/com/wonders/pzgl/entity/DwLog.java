package com.wonders.pzgl.entity;

import java.util.Date;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Name;
import org.nutz.dao.entity.annotation.Table;

@Table("DW_LOG")
public class DwLog {
	@Name
	@Column("ID")
	private String id;
	@Column("OPERATE_TYPE")
	private String operateType;
	@Column("OPERATE_DATE")
	private Date operateDate;
	@Column("OPERATE_RESULT")
	private String operateResult;
	@Column("QUERY_CONDITION")
	private String queryCondition;
	@Column("OPERATE_USER")
	private String operateUser;
	@Column("QUERY_TABLE")
	private String queryTable;
	@Column("QUERY_COUNT")
	private String queryCount;
	
	@Column("OPERATE_DEPT")
	private String operateDept;
	@Column("QUERY_REASON")
	private String queryReason;
	@Column("COUNT_TYPE")
	private String countType;
	public String getCountType() {
		return countType;
	}
	public void setCountType(String countType) {
		this.countType = countType;
	}
	public String getQueryReason() {
		return queryReason;
	}
	public void setQueryReason(String queryReason) {
		this.queryReason = queryReason;
	}
	public String getOperateDept() {
		return operateDept;
	}
	public void setOperateDept(String operateDept) {
		this.operateDept = operateDept;
	}
	public String getQueryCount() {
		return queryCount;
	}
	public void setQueryCount(String queryCount) {
		this.queryCount = queryCount;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOperateType() {
		return operateType;
	}
	public void setOperateType(String operateType) {
		this.operateType = operateType;
	}
	public Date getOperateDate() {
		return operateDate;
	}
	public void setOperateDate(Date operateDate) {
		this.operateDate = operateDate;
	}
	public String getOperateResult() {
		return operateResult;
	}
	public void setOperateResult(String operateResult) {
		this.operateResult = operateResult;
	}
	public String getQueryCondition() {
		return queryCondition;
	}
	public void setQueryCondition(String queryCondition) {
		this.queryCondition = queryCondition;
	}
	public String getOperateUser() {
		return operateUser;
	}
	public void setOperateUser(String operateUser) {
		this.operateUser = operateUser;
	}
	public String getQueryTable() {
		return queryTable;
	}
	public void setQueryTable(String queryTable) {
		this.queryTable = queryTable;
	}
}
